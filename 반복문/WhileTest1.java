package 반복문;

public class WhileTest1 {
	//자동완성 컨트롤+spacebar
	//실행 컨트롤+f11
	public static void main(String[] args) {
		int num = 3; //시작값
		while (num < 5) {/// 조건이 Ture이면 가로 안에 것을 실행하는 것임 // 조건
			System.out.println("환영합니다.");
			num++; // 증감값
			//num++ = num = num + 1; 같은 의미 1씩 증가해라~
		}				
	}
}
