package 증감연산자;

public class 증감연산자3 {

	public static void main(String[] args) {
		int x = 3;
		int y = 5;
		int z = ++x + y ++;
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);

		int k = --x + y --;
		System.out.println(x);
		System.out.println(y);
		System.out.println(k);
	}
}
